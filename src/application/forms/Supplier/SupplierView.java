    private void btnUpdateSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateSupplierActionPerformed
         int selectedRow = tblSupplier.convertRowIndexToModel(tblSupplier.getSelectedRow());

        SupplierUpdate supplierUpdate = new SupplierUpdate(this, true);
        Supplier supplier = new Supplier();
        supplier.setSupplierId(Integer.valueOf(modelSupplier.getValueAt(selectedRow, 0).toString()));
        supplier.setSupplierName(modelSupplier.getValueAt(selectedRow, 1).toString());
        supplier.setSupplierZip(modelSupplier.getValueAt(selectedRow, 2).toString());
        supplier.setSupplierCurrnecy(modelSupplier.getValueAt(selectedRow, 3).toString());
        supplier.setSupplierCountry(modelSupplier.getValueAt(selectedRow, 4).toString());
        supplier.setSupplierFax(modelSupplier.getValueAt(selectedRow, 5).toString());
        supplierUpdate.setSupplierUpdate(supplier);
        supplierUpdate.setVisible(true);
        supplierController.getAllSupplier(modelSupplier);
        checkBtnSupplier();
        txtSupplierSearch.requestFocus();
    }//GEN-LAST:event_btnUpdateSupplierActionPerformed

    private void txtSupplierSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSupplierSearchKeyReleased
        final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(modelSupplier);
        tblSupplier.setRowSorter(sorter);
        sorter.setRowFilter(RowFilter.regexFilter("(?i)" + txtSupplierSearch.getText()));
        checkBtnSupplier();
//        in here "?!" act as "like" operator in sql 
    }//GEN-LAST:event_txtSupplierSearchKeyReleased

