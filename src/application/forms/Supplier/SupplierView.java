    private void btnDeleteSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSupplierActionPerformed
        //Delete button action
         int status = JOptionPane.showConfirmDialog(this, "Do you want to delete this Supplier ?", "Delete Supplier", JOptionPane.YES_NO_OPTION);
        if (status == JOptionPane.YES_OPTION) {
            int selectedRow = tblSupplier.convertRowIndexToModel(tblSupplier.getSelectedRow());
            int supplierId = Integer.valueOf(modelSupplier.getValueAt(selectedRow, 0).toString());
            Supplier supplier = supplierController.findSupplierById(supplierId);
            supplier.setStatus(false);
            supplier.setDeletedAt(new Date());
            if (supplierController.update(supplier)) {
                modelSupplier.removeRow(selectedRow);
                checkBtnSupplier();
                JOptionPane.showMessageDialog(this, "Supplier deleted successfully.", "Deletion Message", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Error in Supplier deleting.", "Error Message", JOptionPane.ERROR_MESSAGE);
            }
        }
        txtSupplierSearch.requestFocus();
    }//GEN-LAST:event_btnDeleteSupplierActionPerformed

